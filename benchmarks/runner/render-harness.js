#!/usr/bin/env node
/**
 * Renders the harness skill templates (templates/*.md + templates/hooks/*)
 * into a ready-to-use .claude/skills/project-harness/ tree for benchmarking.
 *
 * This is a simplified version of what the real wizard does. We do NOT run
 * the full interactive wizard — we just substitute {{VAR}} placeholders
 * with sensible defaults so the skill SKILL.md files are valid and
 * resolvable by `claude -p "/project-harness <task>"`.
 *
 * Output structure under <target>/.claude/skills/project-harness/:
 *   SKILL.md                (from templates/orchestrator.md)
 *   plan/SKILL.md           (from templates/plan.md)
 *   debug/SKILL.md          (from templates/debug.md)
 *   implement/SKILL.md      (from templates/implement.md)
 *   verify/SKILL.md         (from templates/verify.md)
 *   project-config.yaml     (minimal config driving flag-gated behavior)
 *   references/classification.md  (stub — full rules in real wizard)
 *
 * Templates are copied verbatim with placeholder substitution; no semantic changes.
 */

import { readFile, writeFile, mkdir, readdir, copyFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const REPO_ROOT = path.resolve(__dirname, '..', '..');
const TEMPLATES_DIR = path.join(REPO_ROOT, 'templates');

/**
 * Minimal substitution map for the default-flagged project-harness templates.
 * Leaves conditional blocks like {{#has_ui}}...{{/has_ui}} collapsed to the
 * block contents if the flag is true, empty if false.
 */
function getSubstitutions(stack) {
  const isNextJs = stack === 'nextjs-supabase';
  return {
    PROJECT_NAME: isNextJs ? 'Benchmark Next.js Project' : 'Benchmark FastAPI Project',
    DESCRIPTION: 'Benchmark harness — automates classify/plan/implement/verify pipeline.',
    RUN_OPTIONS: '',
    LANGUAGE: isNextJs ? 'TypeScript' : 'Python',
    LINT_CMD: isNextJs ? 'npm run lint' : 'ruff check .',
    TYPECHECK_CMD: isNextJs ? 'npm run typecheck' : 'mypy app/',
    FORMAT_CMD: isNextJs ? 'npm run format' : 'black .',
    TEST_CMD: isNextJs ? 'npm test' : 'pytest',
    HAS_UI: isNextJs ? 'true' : 'false',
    HAS_DATABASE: 'true',
    DB_TYPE: isNextJs ? 'supabase' : 'postgres',
  };
}

/**
 * Simple template substitution:
 *   {{VAR}} → value (string replace)
 *   {{#FLAG}} ... {{/FLAG}} → contents if flag is truthy, else empty
 * This is intentionally simpler than a real mustache engine.
 */
function substitute(template, subs) {
  let result = template;
  // Block-conditional {{#FLAG}}...{{/FLAG}}
  for (const [k, v] of Object.entries(subs)) {
    const truthy = v && v !== 'false' && v !== 'null';
    const blockRegex = new RegExp(`\\{\\{#${k}\\}\\}([\\s\\S]*?)\\{\\{/${k}\\}\\}`, 'g');
    result = result.replace(blockRegex, truthy ? '$1' : '');
  }
  // Simple scalar {{VAR}}
  for (const [k, v] of Object.entries(subs)) {
    result = result.replaceAll(`{{${k}}}`, String(v ?? ''));
  }
  // Any remaining {{XXX}} → remove (unsupported placeholder)
  result = result.replace(/\{\{[A-Z_][A-Z_0-9]*\}\}/g, '');
  return result;
}

async function renderSkill(templateFile, targetPath, subs) {
  const src = path.join(TEMPLATES_DIR, templateFile);
  if (!existsSync(src)) throw new Error(`Template not found: ${src}`);
  const tpl = await readFile(src, 'utf8');
  const rendered = substitute(tpl, subs);
  await mkdir(path.dirname(targetPath), { recursive: true });
  await writeFile(targetPath, rendered);
}

function getProjectConfig(stack) {
  const isNextJs = stack === 'nextjs-supabase';
  return `# Benchmark project-config.yaml (auto-generated by render-harness.js)
# Minimal config to drive flag-gated skill behavior.

project_name: ${isNextJs ? 'benchmark-nextjs' : 'benchmark-fastapi'}
stack: ${stack}
language: ${isNextJs ? 'typescript' : 'python'}

flags:
  has_ui: ${isNextJs ? 'true' : 'false'}
  has_backend: true
  has_database: true
  has_cache: false
  has_auth: true
  has_realtime: false
  visual_qa_capable: false

commands:
  lint: "${isNextJs ? 'npm run lint' : 'ruff check .'}"
  typecheck: "${isNextJs ? 'npm run typecheck' : 'mypy app/'}"
  format: "${isNextJs ? 'npm run format' : 'black .'}"
  test: "${isNextJs ? 'npm test' : 'pytest'}"

enforcement:
  level: standard
  protected_files: true
  secret_guard: true
  pattern_guard: true
  db_safety: true
  post_edit_lint: true
  post_edit_typecheck: true

pipeline:
  regression_loop:
    max_attempts: 2
  verify_agents:
    - security-reviewer
    - code-reviewer
`;
}

function getClassificationRef() {
  return `# Classification reference (benchmark stub)

Minimal classification rules for the benchmark harness.

## Type inference

- If task mentions "bug", "broken", "fails", "error" → **bugfix**
- If task mentions "refactor", "rename", "extract", "consolidate" → **refactor**
- If task mentions "config", "settings", "env" → **config**
- Otherwise → **feature**

## Flag inference

Read \`project-config.yaml\` — all flags are pre-set for the benchmark (no auto-detection needed).

## Debug complexity

- High: "race condition", "intermittent", "production", "data corruption"
- Medium: "test failure", "regression"
- Low: everything else

## Output

\`\`\`
🏷️ Classification complete
   → type: feature | has_ui: <from config> | has_backend: true | has_database: true
\`\`\`
`;
}

/**
 * Render the full harness skill tree into <target>/.claude/skills/project-harness/
 */
export async function renderHarness(targetDir, stack) {
  const subs = getSubstitutions(stack);
  const skillRoot = path.join(targetDir, '.claude', 'skills', 'project-harness');

  // Orchestrator (project-harness) — at root
  await renderSkill('orchestrator.md', path.join(skillRoot, 'SKILL.md'), subs);
  // Sub-skills — separate skill dirs at .claude/skills/project-<name>/
  const skillsBase = path.join(targetDir, '.claude', 'skills');
  await renderSkill('plan.md', path.join(skillsBase, 'project-plan', 'SKILL.md'), subs);
  await renderSkill('debug.md', path.join(skillsBase, 'project-debug', 'SKILL.md'), subs);
  await renderSkill('implement.md', path.join(skillsBase, 'project-implement', 'SKILL.md'), subs);
  await renderSkill('verify.md', path.join(skillsBase, 'project-verify', 'SKILL.md'), subs);

  // project-config.yaml at harness skill root
  await writeFile(path.join(skillRoot, 'project-config.yaml'), getProjectConfig(stack));
  // references/classification.md
  await mkdir(path.join(skillRoot, 'references'), { recursive: true });
  await writeFile(path.join(skillRoot, 'references', 'classification.md'), getClassificationRef());

  // Initialize empty state dirs so skills can write to them
  await mkdir(path.join(skillRoot, 'state', 'results'), { recursive: true });
  await mkdir(path.join(skillRoot, 'state', 'handoffs'), { recursive: true });
}

// CLI entrypoint for ad-hoc rendering
if (process.argv[1] && process.argv[1].endsWith('render-harness.js')) {
  const targetDir = process.argv[2];
  const stack = process.argv[3] ?? 'nextjs-supabase';
  if (!targetDir) {
    console.error('Usage: node render-harness.js <targetDir> [stack]');
    process.exit(1);
  }
  renderHarness(targetDir, stack).then(() => {
    console.log(`[render] Harness rendered into ${targetDir}/.claude/skills/`);
  }).catch((err) => {
    console.error(`[render] FAILED: ${err.message}`);
    process.exit(2);
  });
}
